//equal == check only values not datatype
//strictly_equal === check value + datatype

//equal =
let equal = 5 == "5";
console.log(equal);

//strictly equal =
let strictly_equal = 5 === "5";
console.log(strictly_equal);

//not equal =
let not_equal = 5 != "5";
console.log(not_equal);

//strictly not equal =
let stricty_notequal = 5 !== "5";
console.log(stricty_notequal);

